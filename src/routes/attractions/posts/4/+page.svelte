<h1>Ford Thunderbird Super Coupe (Форд Тандербёрд Супер Купе) 1989-2000</h1>
<p>Предпоследнее поколение автомобилей Ford Thunderbird было представлено в середине 80-х гг. и оказалось намного более элегантным и обтекаемым, чем предыдущие разработки американского концерна Ford. Новый Thunderbird стал кандидатом на участие в престижных трековых гонках NASCAR. Из дорожных машин представлял интерес 2,3-литровый вариант Turbo Coupe (Турбо Купе). Он отличался особой 4-рычажной задней подвеской с двумя дополнительными продольными амортизаторами.
    Машина имела хорошие характеристики, но недостаточные для того, чтобы отучить американцев от моторов V8. Поэтому было решено на Super Coupe 90-х гг. использовать механический, а не турбонад-дув. Двигатель V6 (3,8 л) с алюминиевыми головками блока цилиндров получил нагнетатель Rootes (Руте) (максимальное давление наддува - 0,84 кг/смг) и промежуточный охладитель, что увеличило мощность до 210 л.с. при 4000 об/мин, а максимальный крутящий момент - до 427 Н*м при 2600 об/мин. Это соответствовало показателям моторов V8. Максимальная скорость возросла почти до 225 км/ч, что заставило отказаться от прежней задней подвески в пользу независимой с двумя нижними треугольными рычагами, верхними направляющими штангами, винтовыми пружинами и стабилизатором поперечной устойчивости.
</p>
