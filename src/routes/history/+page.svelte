<p>Энергетический кризис 70-80-х гг. заставил Ford уменьшить размеры своих автомобилей, снизить мощность двигателей и упростить их стиль. Как и его конкуренты, Ford стал устанавливать связи с японскими компаниями, что позволило начать массовый выпуск переднеприводных машин. В 80-е гг. полноразмерные американские седаны типа LTD ушли в прошлое. Покупатели останавливали свой выбор на семейных автомобилях уменьшенных размеров вроде Fairmont (Фэйрмонт). Затем настало время моделей, выполненных в европейском стиле. Эта тенденция была хорошо заметна на серии Thunderbird, когда автомобили из типичных представителей американского дизайна превратились в образцы плавных обтекаемых форм. Другим подобным примером являлась модель Tempo.
    </p>
<img src={image12} alt="image" >
