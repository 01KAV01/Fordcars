<p>Несмотря на смену ориентиров, Ford не перестал быть самим собой, выпуская наряду с заурядными моделями и такие образцы как Mustang SVO с турбонаддувом и обновленный Mustang GT. Наиболее привлекательные автомобили Ford выпускал обычно под названием Lincoln Continental (Линкольн Континентал). 
    </p>
<img src={image14} alt="image" >
<img src={image15} alt="image" >
<p>Предпоследнее поколение автомобилей Ford Thunderbird было представлено в середине 80-х гг. и оказалось намного более элегантным и обтекаемым, чем предыдущие разработки американского концерна Ford. Новый Thunderbird стал кандидатом на участие в престижных трековых гонках NASCAR. Из дорожных машин представлял интерес 2,3-литровый вариант Turbo Coupe (Турбо Купе). Он отличался особой 4-рычажной задней подвеской с двумя дополнительными продольными амортизаторами.
    В 1999 г. на автосалоне в Детройте показали прототип нового Thunderbird с 2-местным кузовом, в котором сочетались современные идеи и классические формы старого доброго "Громовержца" 50-х гг. (форма передка, интерьер салона). Идея такого автомобиля оказалась востребованной, и руководство Ford решилось на серийное производство модели. На этом заканчивается краткая история автомобилей Ford до 2000-х.
    </p>
