<p>Cвой первый автомобиль Генри Форд (Henry Ford) построил 1896 г. в мастерской рядом со своим домом в Детройте. Произошло это задолго до основания им собственной компании. Поработав на знаменитого изобретателя Томаса Эдисона (Thomas Edison), затем на фирме Detroit Automobile (Детройт Отомобайл), он основал в 1903 г. Ford Motor Company.
    Первые автомобили Ford были ручного изготовления, большими и дорогими.
    </p>
<img src={image1} alt="image" >
<p>Прорыв произошел в 1908 г. благодаря модели "Т". Продавая эти машины по самой низкой цене в единственном варианте исполнения, Форд стремился "поставить Америку на колеса". Кроме того, на своем заводе в Хайлэнд Парке в 1913 г. он внедрил движущуюся производственную линию, позволившую значительно снизить себестоимость изготовления.  В результате с 1908 по 1927 г. было собрано 15 млн. автомобилей модели "Т", а компания Ford стала лидером на рынке США. 
    </p>
<img src={image2} alt="image" >
<p>На смену модели "Т" пришла модель "А". Конструктивно машина оказалась вполне современной: она имела 3-ступенчатую коробку передач, тормоза на всех колесах, получила ряд новинок для мирового автомобилестроения. Впервые на ней использовали закаленное ветровое стекло, гидравлические амортизаторы, впервые машина с кузовом универсал пошла в серийное производство. Хотя модель "А" и последовавшие за ней автомобили с двигателем V8 оказались очень удачными, компания вошла в серьезный конфликт с профсоюзами и в 30-е гг. постепенно уступила лидерство.
    </p>
<img src={image3} alt="image" >
<p> За моделью "А" последовала менее успешная модель "АВ", а затем более солидная "V8", названная "Последним механическим триумфом Форда". Машина получила шасси от модели "АВ", включая ее не очень эффективные тормоза. Несмотря на ряд проблем с шестернями, автомобиль стал в 30-е гг. основой производственной программы Ford. К этому времени американские автомобили, включая "V8", каждый год подвергались стилистическим изменениям. Перед смертью Генри Форда компания находилась на грани банкротства. Спас положение внук Генри Форд II, пришедший к руководству после второй мировой войны.
    </p>
<img src={image4} alt="image" >
<p>К этому времени Ford уже выпускал модели Mercury (Меркьюри) и Lincoln (Линкольн), владел отделениями в Европе и на других континентах. И все же основной продукцией концерна оставались дешевые семейные автомобили. Продажи Ford в США увеличились с 480 тыс. единиц в 1946 г. до 1,5 млн. в 1959 г. и до 2 млн. в 1970 г. Все это время Ford боролся за звание самой успешной американской автомобилестроительной компании. Но занимал только второе место за General Motors. 
    </p>
<img src={image5} alt="image" >
<img src={image6} alt="image" >
<p>Время от времени Ford позволял себе создание таких автомобилей, как 2-местный Thunderbird 50-х гг., пользовавшийся феноменальным успехом Mustang (Мустанг), 7-литровые Galaxie (Гэлекси) и Torino (Торино) - все 60-х гг., и 1,6-литровый Pinto (Пинто) начала 70-х.
    </p>
